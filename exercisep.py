# Code challenge 9:  9. Name Formatter
# Ask for a full name like “john doe”
# Capitalize both parts (output: “John Doe”)
# (Use .capitalize() and string slicing logic)

firstName = input("Enter Your First Name: ")
lastName = input("Enter your last Name: ")

fullName = firstName.capitalize() + " " + lastName.capitalize()

print(fullName)


