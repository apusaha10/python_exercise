# challenge 19:  7: Compare lengths of two lists
# You have two lists. Check which one has more items.

# list1 = [1, 2, 3]
# list2 = [4, 5]
# # Output: List 1 is longer

list1 = [1, 2, 3]
list2 = [4, 5]


